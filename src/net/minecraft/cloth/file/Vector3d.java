package net.minecraft.cloth.file;

public class Vector3d {
    private double x;
    private double y;
    private double z;

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

    public double getZ() {
        return this.z;
    }

    public void setVector3d(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
